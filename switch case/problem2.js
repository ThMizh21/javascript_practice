let character = "p";

switch (character) {
    case "a":
    case "e":
    case "i":
    case "o":
    case "u":
    case "A":
    case "E":
    case "I":
    case "O":
    case "U":
        console.log("Vowel");
        break;

    default:
        console.log("It's a consonant");
        break;


}